package practica_complementario_clase_abstracta.ejercicio2;

public class Asteroide {

    private int x;

    private int y;

    public Asteroide(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
